class CCTypes():
    ENUM = 1
    FLAG = 2
    VALUE = 3

class CCItem():
    name = ""
    index = ""
    byte = 0
    bit = 0
    size = 0
    itemlist = []
    type = None

    def __init__(self, name, index, byte, bit, size, items=None, type=CCTypes.ENUM):
        self.name = name
        self.index = index
        self.byte = byte
        self.bit = bit
        self.size = size
        if type == CCTypes.ENUM:
            self.itemlist = ["Unknown" for i in range(0, 2**size)]
            self.itemlist[0] = "Not configured"
            if items is not None:
                for item in items:
                    self.itemlist[item[0]] = item[1]
        else:
            self.itemlist = items if items is not None else []
        self.type = type

    @property
    def items(self):
        return len(self.itemlist)

    @property
    def item(self):
        return self.itemlist

    def decode(self, value, other=None, other2=None):
        try:
            s = "[ %3d ] %s:%s" % (self.index, self.name, (48 - len(self.name)) * " ")
            v = value[self.byte]
            d = (v >> self.bit) & ((2**self.size) - 1)
            if other is None:
                if self.type == CCTypes.ENUM:
                    s = s + "%02X %s" % (d, self.itemlist[d])
                elif self.type == CCTypes.VALUE:
                    s = s + "%02X [%d]" % (d, d)
                elif self.type == CCTypes.FLAG:
                    s = s + "%02X %s" % (d, "True" if d else "False")
            else:
                v2 = other[self.byte]
                d2 = (v2 >> self.bit) & ((2**self.size) - 1)
                if other2 is not None:
                    v3 = other2[self.byte]
                    d3 = (v3 >> self.bit) & ((2**self.size) - 1)
                    if self.type == CCTypes.ENUM:
                        s = s + "%-56s [%02X] - [%02X] %56s - %-56s [%02X]" % (self.itemlist[d], d, d2, self.itemlist[d2] if d != d2 else "--",  self.itemlist[d3] if d != d3 else "--", d3)
                    elif self.type == CCTypes.VALUE:
                        s = s + "                                                          %02X  -  %02X                                                           -                                                           %02X" % (d, d2, d3)
                    elif self.type == CCTypes.FLAG:
                        s = s + "%02X %s" % (d, "True" if d else "False")
                    else:
                        s = s + "???"
                else:
                    if self.type == CCTypes.ENUM:
                        s = s + "%-56s [%02X] - [%02X] %56s" % (self.itemlist[d], d, d2, self.itemlist[d2] if d != d2 else "--")
                    elif self.type == CCTypes.VALUE:
                        s = s + "                                                          %02X  -  %02X" % (d, d2)
                    elif self.type == CCTypes.FLAG:
                        s = s + "%02X %s" % (d, "True" if d else "False")
                    else:
                        s = s + "???"
            return s
        except Exception as e:

            print(e)
            return None



